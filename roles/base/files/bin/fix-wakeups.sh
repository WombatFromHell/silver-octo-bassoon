#!/usr/bin/env bash

# Devices to disable
devices=(
  GPP0 # PCIe bridge
  XH00 # Disable mouse/keyboard hub wakeups
)

CONF_FILE="/etc/tmpfiles.d/fix-wakeups.conf"

install() {
  # Generate new content into a temp file
  TEMP=$(mktemp)
  {
    echo "# Generated by fix-wakeups.sh"
    for dev in "${devices[@]}"; do
      echo "w /proc/acpi/wakeup - - - - $dev"
    done
  } >"$TEMP"

  # If the target file doesn't exist, or it's different, update it
  if [[ ! -f "$CONF_FILE" ]] || ! cmp -s "$TEMP" "$CONF_FILE"; then
    mv "$TEMP" "$CONF_FILE"
    systemd-tmpfiles --create "$CONF_FILE"
    echo "CHANGED: Installed/updated tmpfiles.d configuration."
  else
    rm "$TEMP"
    echo "OK: Configuration up to date."
  fi
}

uninstall() {
  if [[ -f "$CONF_FILE" ]]; then
    rm "$CONF_FILE"
    echo "CHANGED: Uninstalled tmpfiles.d configuration."
  else
    echo "OK: Configuration already absent."
  fi
}

run() {
  for dev in "${devices[@]}"; do
    if grep -q "$dev.*enabled" /proc/acpi/wakeup; then
      if ! echo "$dev" >/proc/acpi/wakeup; then
        echo "Error: Failed to disable $dev" >&2
      else
        echo "Disabling ACPI wakeups on: $dev"
      fi
    fi
  done
}

case "$1" in
install)
  install
  ;;
uninstall)
  uninstall
  ;;
*)
  run
  ;;
esac
